      <h3 class="medium is-adaptive">
        24. Перевозки организованных групп детей
      </h3>
      <p class="regular is-adaptive align-j">
        280. Перевозки организованных групп детей осуществляются автобусами,
        оборудованными в соответствии с требованиями настоящих Правил и с
        предоставлением каждому ребенку отдельного места для сидения. Перевозки
        организованных групп детей относятся к нерегулярным (заказным)
        автомобильным перевозкам.<br />
        <br />
        281. При автомобильных перевозках автобусами групп детей обеспечение
        безопасности движения возлагается на перевозчика, а обеспечение
        безопасного поведения детей - на заказчика и назначенных им
        сопровождающих.<br />
        <br />
        282. Сопровождающие обязаны принимать меры, обеспечивающие безопасную
        перевозку групп детей автобусами.<br />
        <br />
        283. При организации перевозок групп детей в учебные заведения
        перевозчик совместно с администрацией учебных заведений определяет
        маршруты и рациональные места посадки и высадки детей.<br />
        <br />
        284. Площадки, отводимые для ожидающих автобус детей, должны быть
        достаточного размера, чтобы не допускать выхода детей на проезжую
        часть.<br />
        <br />
        285. Если перевозки групп детей осуществляются в темное время суток, то
        площадки должны иметь искусственное освещение.<br />
        <br />
        286. В осенне-зимний период времени площадки должны очищаться от снега,
        льда, грязи.<br />
        <br />
        287. Заказчик перевозок групп детей в учебные заведения регулярно (не
        реже одного раза в месяц) проверяет состояние мест посадки и высадки
        детей.<br />
        <br />
        288. Расписание движения автобусов для перевозки групп детей
        согласовывается с заказчиком перевозок.<br />
        <br />
        289. При возникновении в пути технических неисправностей, угрожающих
        безопасности движения, а также при ухудшении состояния здоровья
        водителей запрещается дальнейшее движение до устранения обстоятельств,
        препятствующих безопасному движению.<br />
        <br />
        290. При автомобильных перевозках групп детей по территории
        Приднестровской Молдавской Республики скорость движения автобусов,
        включая автобусы с мягкими сиденьями, не должна превышать 60
        (шестидесяти) км/ч. На автобусах, выполняющих перевозку групп детей
        (одиночных или следующих колонной), независимо от времени суток должен
        быть включен ближний свет фар и установлены опознавательные знаки
        "Дети.<br />
        <br />
        291. При нерегулярных (заказных) перевозках школьников в пределах города
        (населенных пунктов) перевозчик обязан:<br />
        <br />
        а) обеспечить своевременную доставку школьников в школу и обратно и не
        допускать перегрузки автобуса;<br />
        <br />
        б) устанавливать специальные остановочные знаки с указанием времени
        прохождения автобуса, осуществляющего перевозку школьников;<br />
        <br />
        в) оборудовать автобусы лобовыми и боковыми трафаретами с надписью
        "Школьный" и опознавательными знаками "Дети.
      </p>
