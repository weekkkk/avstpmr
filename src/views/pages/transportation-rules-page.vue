      <h3 class="medium is-adaptive">
        17. Обеспечение безопасного выполнения автомобильных перевозок
        пассажиров
      </h3>
      <p class="regular is-adaptive align-j">
        217. Безопасное выполнение перевозок пассажиров должно обеспечиваться
        автомобильными перевозчиками и операторами автомобильных перевозок
        пассажиров, дорожными организациями и другими лицами, осуществляющими
        транспортную деятельность по перевозке пассажиров.<br />
        <br />
        218. Ответственность за организацию работы по обеспечению автомобильных
        перевозок пассажиров и багажа возлагается на оператора автомобильных
        перевозок и организации, осуществляющие регулярные автомобильные
        перевозки.<br />
        <br />
        219. При непригодности дорожных условий для осуществления автомобильных
        перевозок пассажиров или несоответствия их установленным требованиям
        перевозчик должен принимать меры по приостановлению и (или) изменению
        маршрута перевозок.<br />
        <br />
        220. К водителям, выполняющим автомобильные перевозки пассажиров,
        предъявляются требования, установленные настоящими Правилами и другими
        нормативно-правовыми актами Приднестровской Молдавской Республики.
        Перевозчик может предъявлять к водителям дополнительные требования по
        обеспечению безопасности дорожного движения и перевозок пассажиров, не
        противоречащие действующему законодательству и настоящим Правилам.<br />
        <br />
        221. Перевозчику запрещается понуждать или поощрять водителей
        транспортных средств к нарушению требований безопасности дорожного
        движения.<br />
        <br />
        222. Графики работы водителей должны обеспечивать соблюдение норм
        законодательства Приднестровской Молдавской Республики в части режима
        труда и отдыха водителей.<br />
        <br />
        223. Работа водителей в командировке должна быть организована в
        соответствии с действующим законодательством Приднестровской Молдавской
        Республики.<br />
        <br />
        224. Перевозчик должен обеспечить нахождение эксплуатируемых
        транспортных средств при межсменном хранении на специально
        предназначенных для этого охраняемых стоянках, кроме городских
        маршрутных такси.<br />
        <br />
        225. Перевозчик должен обеспечить контроль технического состояния
        транспортного средства с отметкой в путевом листе перед началом работы.
        Допуск к автомобильным перевозкам пассажиров транспортных средств,
        находящихся в технически неисправном состоянии, запрещается.<br />
        <br />
        226. Перевозчик должен предъявлять эксплуатируемые им транспортные
        средства для их государственного технического осмотра в порядке и с
        периодичностью, установленными законодательством Приднестровской
        Молдавской Республики.<br />
        <br />
        227. Перевозчик имеет право при выполнении автомобильной перевозки
        пассажиров осуществлять контроль за соблюдением положений нормативных
        правовых актов, регламентирующих безопасность перевозки.<br />
        <br />
        228. Перевозчик для обеспечения безопасного выполнения автомобильных
        перевозок пассажиров обязан:<br />
        <br />
        а) организовывать работу по соблюдению требований нормативных правовых
        актов в области обеспечения безопасности дорожного движения и
        автомобильных перевозок пассажиров;<br />
        <br />
        б) иметь службу безопасности движения или назначить на должность,
        связанную с обеспечением безопасных автомобильных перевозок пассажиров,
        ответственное лицо (в случае наличия в подвижном составе организации для
        обслуживания маршрутов более 50 (пятидесяти) транспортных средств);<br />
        <br />
        в) организовывать и контролировать работу водителей;<br />
        <br />
        г) иметь нормативные правовые акты, методические и информационные
        материалы для проведения профилактической работы по организации
        безопасных автомобильных перевозок пассажиров;<br />
        <br />
        д) обеспечивать постоянный контроль за соблюдением действующих
        нормативов по срокам управления, работы, перерывов в работе и отдыха
        водителей;<br />
        <br />
        е) обеспечивать соответствие технического состояния транспортных средств
        требованиям безопасности дорожного движения, не допуская к эксплуатации
        транспортные средства с неисправностями;<br />
        <br />
        ж) организовывать проведение служебного расследования, учет и анализ
        дорожно-транспортных происшествий с участием транспортных средств
        автомобильного перевозчика, а также обеспечить выявление причин,
        способствующих их возникновению;<br />
        <br />
        з) инструктировать работников о порядке действий при получении
        информации о совершении или угрозе совершения акта терроризма, при
        управлении в сложных дорожных и метеорологических условиях, при
        дорожно-транспортном происшествии, пожаре и в других чрезвычайных
        ситуациях;<br />
        <br />
        и) обеспечивать учет данных о квалификации водителей, их стаже работы на
        определенных типах транспортных средств, сроках прохождения медицинского
        переосвидетельствования, участии в дорожно-транспортных происшествиях,
        допущенных нарушениях Правил дорожного движения и требований
        безопасности автомобильных перевозок пассажиров;<br />
        <br />
        к) проверять знание водителями Правил дорожного движения, настоящих
        Правил и других актов законодательства по вопросам безопасности
        дорожного движения и автомобильных перевозок;<br />
        <br />
        л) планировать и проводить мероприятия, направленные на соблюдение
        требований Правил дорожного движения и безопасной перевозки пассажиров,
        доводить до водителей информацию об изменении Правил дорожного
        движения;<br />
        <br />
        м) оперативно доводить до водителей сведения о причинах и
        обстоятельствах известных ему дорожно-транспортных происшествий, а также
        информацию об изменениях условий движения и работы на маршрутах
        автомобильных перевозок пассажиров;<br />
        <br />
        н) проводить мероприятия, направленные на повышение профессиональной
        подготовки водителей, в том числе по безопасной автомобильной перевозке
        пассажиров;<br />
        <br />
        о) осуществлять контроль за обеспечением безопасности дорожного движения
        и автомобильных перевозок пассажиров в процессе выполнения этих
        перевозок, в частности, соблюдения скоростных режимов, норм вместимости
        транспортных средств и маршрутов движения;<br />
        <br />
        п) организовывать в установленном порядке проведение медицинского
        обследования водителей с отметкой в путевом листе перед началом работы,
        а при необходимости - во время работы;<br />
        <br />
        р) обеспечивать учет и анализ данных медицинского обследования
        водителей;<br />
        <br />
        с) устанавливать постоянный контроль за выполнением работниками
        возложенных на них обязанностей по обеспечению безопасности
        автомобильных перевозок пассажиров;<br />
        <br />
        т) координировать работу своих служб (отделов, работников) по
        обеспечению безопасности дорожного движения и автомобильных перевозок
        пассажиров.<br />
        <br />
        229. В случае неисполнения либо ненадлежащего исполнения перевозчиком
        обязанностей по обеспечению безопасности автомобильных перевозок
        пассажиров он несет ответственность в соответствии с действующим
        законодательством Приднестровской Молдавской Республики.
      </p>
