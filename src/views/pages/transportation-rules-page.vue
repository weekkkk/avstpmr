      <h3 class="medium is-adaptive">
        18. Ответственность перевозчика, заказчика автомобильных перевозок
        пассажиров, пассажира, операторов автомобильных перевозок пассажиров
      </h3>
      <p class="regular is-adaptive align-j">
        230. За неисполнение или ненадлежащее исполнение обязательств по
        договору об обслуживании регулярных маршрутов (рейсов) автомобильных
        перевозок пассажиров и багажа, договору о фрахтовании для автомобильных
        перевозок пассажиров стороны несут ответственность, установленную
        законодательством Приднестровской Молдавской Республики, а также
        соглашением сторон.<br />
        <br />
        231. Соглашения сторон об ограничении или устранении установленной
        законодательством Приднестровской Молдавской Республики ответственности
        недействительны, за исключением случаев, когда возможность таких
        соглашений предусмотрена законодательством Приднестровской Молдавской
        Республики.<br />
        <br />
        232. Перевозчик несет имущественную и другую ответственность в
        соответствии с законодательством Приднестровской Молдавской Республики
        за причинение вреда жизни или здоровью пассажира, если это связано с
        выполнением автомобильной перевозки и имело место либо во время
        нахождения пассажира в салоне транспортного средства, либо во время
        посадки или высадки, либо в связи со сдачей или приемом багажа.<br />
        <br />
        233. Перевозчик несет ответственность как за свои действия, так и за
        действия третьих лиц, к услугам которых он прибегает для выполнения
        обязательств, возлагаемых на него в соответствии с договором об
        обслуживании регулярных маршрутов (рейсов) автомобильных перевозок
        пассажиров и багажа, когда эти лица действуют в пределах своих
        обязательств.<br />
        <br />
        234. За неподачу или несвоевременную подачу транспортного средства
        перевозчик несет ответственность перед заказчиком в порядке,
        предусмотренном соглашением сторон. При этом обязательно предъявление
        заказчиком претензии перевозчику не позднее 30 (тридцати) календарных
        дней с даты подачи транспортного средства, установленной соответствующим
        договором.<br />
        <br />
        235. Перевозчик освобождается от ответственности за неподачу или
        несвоевременную подачу транспортного средства, если это произошло
        вследствие непреодолимой силы или других непредотвратимых при данных
        условиях обстоятельств, прекращения или ограничения автомобильных
        перевозок пассажиров в определенном направлении в установленном порядке,
        а также в иных случаях, предусмотренных законодательством
        Приднестровской Молдавской Республики.<br />
        <br />
        236. Перевозчик несет ответственность за несохранность (утрату,
        недостачу, повреждение) багажа, принятого к автомобильной перевозке,
        если не докажет, что несохранность багажа вызвана обстоятельствами,
        которые он не мог предотвратить и устранение которых от него не
        зависело.<br />
        <br />
        237. Ущерб, причиненный при автомобильной перевозке багажа и (или)
        ручной клади, возмещается перевозчиком при:<br />
        <br />
        а) утрате или недостаче багажа - согласно действующему законодательству
        Приднестровской Молдавской Республики;<br />
        <br />
        б) повреждении (порче) багажа или ручной клади по вине перевозчика - в
        размере суммы, на которую снизилась стоимость, а при невозможности
        восстановления - в размере стоимости.<br />
        <br />
        238. Если недостача или повреждение (порча) части багажа влияет на
        стоимость другой части багажа по данной багажной квитанции, то при
        определении размера возмещения учитываются обе части этого багажа.<br />
        <br />
        239. Перевозчик наряду с возмещением установленного ущерба, вызванного
        утратой, недостачей или повреждением (порчей) багажа, возвращает
        пассажиру полученную от него плату за провоз багажа.<br />
        <br />
        240. В случае отмены или переноса рейса автомобильной перевозки
        пассажиров в регулярном сообщении перевозчик или оператор автомобильных
        перевозок пассажиров по требованию пассажира до начала отправления
        автобуса возвращает пассажиру стоимость предъявленного разового билета,
        а также стоимость оплаченных пассажиром дополнительных услуг по
        предстоящей перевозке.<br />
        <br />
        241. При международной, если иное не предусмотрено договором об
        автомобильной перевозке пассажиров, и междугородной перевозках в
        регулярном сообщении за задержку отправления автобуса или опоздание
        прибытия в пункт назначения более чем на 1 час перевозчик уплачивает
        пассажиру неустойку в размере 20 (двадцати) процентов от стоимости
        автомобильной перевозки пассажира, если не докажет, что задержка или
        опоздание имели место вследствие непреодолимой силы, устранения
        неисправностей автобуса, угрожающих жизни и здоровью пассажиров, или
        иных обстоятельств, не зависящих от перевозчика. В случае отказа
        пассажира от автомобильной перевозки по причине такой задержки автобуса
        перевозчик или оператор автомобильных перевозок пассажиров обязан
        возвратить пассажиру провозную плату в полном объеме.<br />
        <br />
        242. Вред, причиненный перевозчиком жизни или здоровью пассажира,
        подлежит возмещению в соответствии с Гражданским кодексом
        Приднестровской Молдавской Республики, если законодательством
        Приднестровской Молдавской Республики или договором об обслуживании
        регулярных маршрутов (рейсов) перевозок пассажиров и багажа не
        предусмотрена повышенная ответственность перевозчика.<br />
        <br />
        243. Пассажир обязан возместить ущерб, причиненный по его вине имуществу
        перевозчика, а также имуществу третьих лиц, за которое несет
        ответственность перевозчик. Размер возмещения определяется величиной
        причиненного ущерба.<br />
        <br />
        244. Ответственность за иные отклонения от условий договора об
        обслуживании регулярных маршрутов (рейсов) автомобильных перевозок
        пассажиров и багажа, которые привели к дополнительным расходам
        перевозчика, определяется соглашением сторон и законодательством
        Приднестровской Молдавской Республики.<br />
        <br />
        245. При проезде в автобусе без билета (с недействительным билетом, в
        том числе не погашенным с помощью устройства для гашения) или провозе
        багажа без оплаты пассажир должен оплатить проезд или провоз багажа и
        уплатить штраф в соответствии с законодательством Приднестровской
        Молдавской Республики.<br />
        <br />
        246. При проезде пассажира в автобусе без соответствующих документов,
        подтверждающих право на льготы по оплате проезда на автомобильном
        транспорте, пассажир должен оплатить полную стоимость проезда.<br />
        <br />
        247. Случаи исключения и ограничения ответственности, установленные
        гражданским законодательством Приднестровской Молдавской Республики и
        настоящими Правилами для заказчиков и перевозчиков, распространяются
        также на операторов автомобильных перевозок пассажиров, представляющих
        интересы заказчиков и перевозчиков.<br />
        <br />
        248. В случае расторжения договора об обслуживании регулярных маршрутов
        (рейсов) автомобильных перевозок пассажиров и багажа в одностороннем
        порядке по инициативе перевозчика последний уведомляет об этом
        исполнительный орган государственной власти, к ведению которого отнесены
        вопросы управления транспортом (орган местного государственного
        управления), не позднее чем за 30 (тридцать) календарных дней.
