      <h3 class="medium is-adaptive">
        7. Требования, предъявляемые к электрическому транспорту
      </h3>
      <p class="regular is-adaptive align-j">
        46. Перевозчик, осуществляющий перевозку пассажиров и багажа
        электрическим транспортом, должен соответствовать требованиям
        безопасности, установленным действующим законодательством
        Приднестровской Молдавской Республики, и иметь документы, подтверждающие
        соответствие требованиям безопасности.<br />
        <br />
        47. В салоне электрического транспорта должна быть информация о маршруте
        движения, стоимости проезда пассажира и провоза багажа, а также иная
        необходимая информация.<br />
        <br />
        48. В электрическом транспорте перевозчик создает необходимые удобства
        для проезда инвалидов и пассажиров с детьми.<br />
        <br />
        49. Пассажир имеет права и исполняет обязанности, предусмотренные
        настоящими Правилами, в части, касающейся проезда на регулярных
        маршрутах (рейсах) автомобильных перевозок.<br />
        <br />
        50. Перевозчик, обслуживающий регулярные маршруты (рейсы) электрического
        транспорта, имеет права и исполняет обязанности, предусмотренные
        настоящими Правилами, в части, касающейся обслуживания регулярных
        маршрутов (рейсов) автомобильных перевозок.<br />
        <br />
        51. Электрический транспорт осуществляет пассажирские перевозки по
        определенным маршрутам (рейсам) согласно расписанию движения за плату в
        соответствии с установленными действующим законодательством
        Приднестровской Молдавской Республики тарифами.<br />
        <br />
        52. Движение электрического транспорта организуется перевозчиком в
        соответствии с расписанием движения, утверждаемым органами местного
        государственного управления.
      </p>
    </div>
    <div class="block">
      <h3 class="medium is-adaptive">
        8. Порядок открытия новых регулярных маршрутов (рейсов) автомобильных
        перевозок пассажиров и багажа
      </h3>
      <p class="regular is-adaptive align-j">
        53. Регулярные автомобильные перевозки пассажиров и багажа
        осуществляются транспортными средствами по утвержденным соответствующими
        исполнительными органами государственной власти регулярным маршрутам
        (рейсам):<br />
        <br />
        а) организация новых, изменение или закрытие существующих пригородных,
        междугородных, международных регулярных маршрутов (рейсов)
        осуществляются исполнительным органом государственной власти, к ведению
        которого отнесены вопросы управления транспортом;<br />
        <br />
        б) организация новых, изменение или закрытие существующих городских
        регулярных маршрутов (рейсов) осуществляется органами местного
        государственного управления.<br />
        <br />
        Отношения по организации регулярных автомобильных перевозок пассажиров и
        багажа между перевозчиками и исполнительными органами государственной
        власти носят административно-правовой характер.<br />
        <br />
        54. Схемы движения (с указанием улиц) автомобильного транспорта общего
        пользования на регулярных пригородных, междугородных и международных
        маршрутах (рейсах) в пределах города (населенного пункта)
        устанавливаются исполнительным органом государственной власти, к ведению
        которого отнесены вопросы управления транспортом, с учетом существующей
        городской маршрутной сети по согласованию с органами местного
        государственного управления.<br />
        <br />
        Изменение схемы движения на регулярном маршруте (рейсе) может
        осуществляться только в пределах начального или конечного пункта
        существующего регулярного маршрута (рейса). Продление или сокращение
        регулярных маршрутов (рейсов) до другого населенного пункта
        запрещается.<br />
        <br />
        55. Инициаторами открытия новых регулярных маршрутов (рейсов)
        автомобильных перевозок пассажиров и багажа могут выступать
        исполнительный орган государственной власти, к ведению которого отнесены
        вопросы управления транспортом, органы местного государственного
        управления соответствующих административно-территориальных единиц и иные
        заинтересованные лица.<br />
        <br />
        Открытие нового регулярного международного маршрута (рейса)
        осуществляется только с автовокзалов и автостанций Приднестровской
        Молдавской Республики.<br />
        <br />
        56. При формировании маршрутной сети регулярных городских маршрутов
        (рейсов) органы местного государственного управления руководствуются
        приоритетом связи между районами города (населенного пункта).<br />
        <br />
        57. Для решения вопросов по открытию регулярных маршрутов (рейсов)
        инициатор разрабатывает проект схемы маршрута и расписания движения по
        нему и направляет их:<br />
        <br />
        а) по регулярным маршрутам (рейсам) пригородного, междугородного и
        международного сообщения - в исполнительный орган государственной
        власти, к ведению которого отнесены вопросы управления транспортом;<br />
        <br />
        б) по регулярным маршрутам (рейсам) городского сообщения - в органы
        местного государственного управления соответствующих
        административно-территориальных единиц.<br />
        <br />
        58. Для открытия нового регулярного городского, пригородного,
        междугороднего маршрута (рейса) или изменения расписания движения или
        периодичности обслуживания по регулярному маршруту перевозчик направляет
        в исполнительный орган государственной власти, в ведении которого
        находятся вопросы управления транспортом (орган местного
        государственного управления), следующие документы:<br />
        <br />
        а) предложения о возможности открытия нового регулярного маршрута
        (рейса);<br />
        <br />
        б) проекты расписаний движения и схем движения по регулярному маршруту
        (рейсу).<br />
        <br />
        59. В случае, если перевозчик обратился впервые в исполнительный орган
        государственной власти, к ведению которого отнесены вопросы управления
        транспортом, для открытия нового регулярного пригородного,
        междугородного маршрута (рейса), то он должен дополнительно представить
        документы, подтверждающие соответствие его требованиям, установленным
        пунктом 70 настоящих Правил.<br />
        <br />
        60. Для открытия и закрепления нового регулярного международного
        маршрута (рейса), если начальный или конечный пункт регулярного
        международного маршрута находится на территории Приднестровской
        Молдавской Республики, или изменения расписания движения или
        периодичности обслуживания по регулярному маршруту заинтересованное лицо
        предварительно направляет в исполнительный орган государственной власти,
        к ведению которого отнесены вопросы управления транспортом, следующие
        документы:<br />
        <br />
        а) предложения о возможности открытия нового регулярного маршрута;<br />
        <br />
        б) проекты расписаний движения и схем движения по регулярному
        маршруту;<br />
        <br />
        в) лицензию на осуществление международных автомобильных перевозок
        пассажиров;<br />
        <br />
        г) копии официальных документов (приказ, разрешение) компетентных
        органов управления транспортом другого государства и их перевода на
        государственный язык, подтверждающих, что регулярный международный
        маршрут (рейс) закреплен за данным перевозчиком.<br />
        <br />
        д) договор о совместной деятельности по регулярной международной
        перевозке пассажиров и багажа с приднестровским перевозчиком (только для
        иностранных перевозчиков);<br />
        <br />
        В случае, если перевозчик обратился впервые в исполнительный орган
        государственной власти, к ведению которого отнесены вопросы управления
        транспортом, для открытия нового регулярного международного маршрута
        (рейса), то он должен представить дополнительно документы,
        подтверждающие соответствие его требованиям, установленным пунктом 70
        настоящих Правил.<br />
        <br />
        61. Исполнительный орган государственной власти, к ведению которого
        отнесены вопросы управления транспортом, вправе до получения
        перевозчиком официальных документов компетентных органов управления
        транспортом другого государства предварительно согласовать перевозчику
        проекты расписаний движения и схем движения по регулярному маршруту,
        после чего перевозчик направляет данные документы в соответствующий
        компетентный орган управления транспорта государства, по территории
        которого будет пролегать новый регулярный маршрут.<br />
        <br />
        Исполнительный орган государственной власти, к ведению которого отнесены
        вопросы управления транспортом, при наличии всех документов,
        предусмотренных частью первой настоящего пункта, принимает решения об
        открытии нового международного регулярного маршрута.<br />
        <br />
        62. Исполнительный орган государственной власти, к ведению которого
        отнесены вопросы управления транспортом (орган местного государственного
