        <br />
        4) международные - перевозка пассажиров транспортными средствами за
        пределы Приднестровской Молдавской Республики;<br />
        <br />
        б) по режиму выполнения:<br />
        <br />
        1) обычные - перемещение пассажиров и багажа со всеми остановками
        согласно расписанию движения.<br />
        <br />
        2) скоростные - перемещение пассажиров и багажа с ограниченным
        количеством промежуточных остановок;<br />
        <br />
        3) экспрессные - перемещение пассажиров и багажа от начального до
        конечного пункта маршрута (рейса) без промежуточных остановок.<br />
        <br />
        4. В соответствии со спецификой обслуживания населения маршруты могут
        иметь несколько режимов работы:<br />
        <br />
        а) постоянные маршруты (рейсы) - действующие в течение установленного
        периода суток, всех дней недели, месяцев;<br />
        <br />
        б) дополнительные маршруты (рейсы) - действующие в ограниченные периоды
        суток, как правило, в часы пик;<br />
        <br />
        в) сезонные маршруты (рейсы) - организуемые в период функционирования
        зон отдыха, спортивно-оздоровительных комплексов, ярмарок и тому
        подобного;<br />
        <br />
        г) временные маршруты (рейсы) - вводимые при возникновении
        непредвиденных ситуаций на улично-дорожной сети, закрытии отдельных
        участков, транспортных узлов и площадей или до проведения конкурса на
        срок не более 12 (двенадцати) месяцев.<br />
        <br />
        д) ночной маршрут (рейс) - маршрут (рейс), установленный для выполнения
        автомобильных перевозок пассажиров в регулярном сообщении в ночное
        время.<br />
        <br />
        5. Перевозчики, осуществляющие пассажирские перевозки, должны
        обеспечивать:<br />
        <br />
        а) потребность населения в перевозках в строгом соответствии с
        действующим законодательством Приднестровской Молдавской Республики;<br />
        <br />
        б) безопасность пассажиров при пользовании транспортным средством;<br />
        <br />
        в) создание максимальных удобств и культуры обслуживания пассажиров на
        автовокзалах, автостанциях, диспетчерско-кассовых пунктах, в
        автобусах;<br />
        <br />
        г) перевозку и сохранность багажа;<br />
        <br />
        д) охрану окружающей среды от отрицательного воздействия автомобильного
        транспорта.<br />
        <br />
        6. Организация международных автомобильных перевозок пассажиров и багажа
        на регулярных маршрутах, являющихся составной частью маршрутной сети
        Приднестровской Молдавской Республики, осуществляется в соответствии с
        Конвенцией о международных автомобильных перевозках пассажиров и багажа
        от 9 октября 1997 года, Конвенцией о дорожном движении от 8 ноября1968
        года, Европейским соглашением, касающимся работы экипажей транспортных
        средств, производящих международные автомобильные перевозки, от 1 июля
        1970 года.<br />
        <br />
        7. При выполнении международных регулярных маршрутов (рейсов)
        иностранными перевозчиками (нерезидентами) запрещается перевозка
        пассажиров между населенными пунктами, расположенными на территории
        Приднестровской Молдавской Республики.<br />
        <br />
        8. Каждому регулярному маршруту, входящему в утвержденную маршрутную
        сеть Приднестровской Молдавской Республики, присваиваются категория
        (городской, пригородный, междугородный, международный), наименование,
        состоящее из указания на начальный пункт отправления и конечный пункт
        назначения, и порядковый номер (за исключением международного
        маршрута).<br />
        <br />
        9. Перевозки пассажиров транспортом общего пользования выполняются по
        обращению любого лица на условиях публичного договора с применением
        фиксированного тарифа на городские и внутриреспубликанские перевозки,
        включая перевозки пассажиров на регулярных международных маршрутах в
        случае, если пассажир перемещается между населенными пунктами в пределах
        республики, установленного действующим законодательством Приднестровской
        Молдавской Республики.<br />
        <br />
        10. Реестр регулярных маршрутов и перечень организаций, осуществляющих
        регулярные перевозки, публикуется ежегодно исполнительным органом
        государственной власти, к ведению которого отнесены вопросы управления
        транспортом, в средствах массовой информации.<br />
        <br />
        Маршрутная сеть регулярных автомобильных перевозок пассажиров и багажа
        принадлежит Приднестровской Молдавской Республике и утверждается
        исполнительным органом государственной власти, к ведению которого
        отнесены вопросы управления транспортом. Формирование маршрутной сети и
        предоставление права перевозки по регулярному маршруту (рейсу),
        включенному в маршрутную сеть, осуществляется исполнительным органом
        государственной власти, к ведению которого отнесены вопросы управления
        транспортом (органом местного государственного управления).
        Предоставление права перевозки по регулярному маршруту (рейсу) не может
        превышать срок более 10 (десяти) лет.<br />
        <br />
        10-1. Перечень социально значимых регулярных маршрутов (рейсов)
        утверждается исполнительным органом государственной власти, к ведению
