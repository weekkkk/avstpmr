      <h3 class="medium is-adaptive">
        13. Взаимодействие с операторами автомобильных перевозок
      </h3>
      <p class="regular is-adaptive align-j">
        147. Организацию продажи билетов от имени перевозчика в соответствии с
        действующим законодательством Приднестровской Молдавской Республики
        осуществляют операторы автомобильных перевозок, в ведении которых
        находятся автовокзалы, автостанции, диспетчерско-кассовые пункты (далее
        - оператор автомобильных перевозок).<br />
        <br />
        148. В случае, если в одном направлении с территории автовокзала,
        автостанции, диспетчерско-кассового пункта отправляются несколько
        автобусов, то оператор автомобильных перевозок производит продажу
        проездных билетов по времени в соответствии с утвержденным
        расписанием.<br />
        <br />
        149. Деятельность операторов автомобильных перевозок по вопросам
        организации обслуживания пассажиров и перевозчиков регулируется
        исполнительным органом государственной власти, к ведению которого
        отнесены вопросы управления транспортом.<br />
        <br />
        150. Осуществление деятельности оператора автомобильных перевозок без
        договора об обслуживании регулярных маршрутов (рейсов) автомобильных
        перевозок пассажиров и багажа с исполнительным органом государственной
        власти, к ведению которого отнесены вопросы управления транспортом, не
        допускается.<br />
        <br />
        151. Исполнительный орган государственной власти, в ведении которого
        находятся вопросы управления транспортом, одновременно с заключением
        договора об обслуживании регулярных маршрутов (рейсов) автомобильных
        перевозок пассажиров и багажа с перевозчиком направляет уведомление о
        заключении указанного договора с приложением утвержденных расписаний
        движения заинтересованным операторам автомобильных перевозок.<br />
        <br />
        152. Оператор автомобильных перевозок в течение 3 (трех) рабочих дней с
        даты получения уведомления с приложением утвержденных расписаний
        движения обязан внести изменения в расписания движения автовокзала
        (автостанции, диспетчерско-кассового пункта) и довести указанную
        информацию до сведения пассажиров в установленном порядке. Для целей
        осуществления регулярных пригородных, междугородных и международных
        автомобильных перевозок пассажиров и багажа перевозчики, получившие
        разрешения на автомобильную перевозку пассажиров по регулярному
        маршруту, заключают с оператором автомобильных перевозок договор о
        совместной деятельности по организации автомобильных перевозок
        пассажиров и багажа на регулярных маршрутах. Договор по организации
        продажи билетов, заключенный между перевозчиком и оператором
        автомобильных перевозок, в обязательном порядке согласовывается с
        исполнительным органом государственной власти, к ведению которого
        отнесены вопросы управления транспортом.<br />
        <br />
        153. Операторы автомобильных перевозок осуществляют продажу билетов
        пассажирам от имени перевозчиков, допущенных в установленном
        законодательством Приднестровской Молдавской Республики к осуществлению
        данного вида деятельности.<br />
        <br />
        154. Обслуживание в соответствии с договором о совместной деятельности
        по организации автомобильных перевозок пассажиров и багажа на регулярных
        маршрутах, заключенным с перевозчиком, осуществляющим перевозку
        пассажиров по регулярным маршрутам (рейсам), осуществляется оператором
        автомобильных перевозок по предъявлению перевозчиками (водителями
        транспортных средств) следующих документов:<br />
        <br />
        а) путевого листа с обязательной отметкой о прохождении предрейсового
        медицинского освидетельствования водителя и технического осмотра
        транспортного средства, билетно-учетного листа по форме, установленной
        действующим законодательством, определяющим порядок применения
        унифицированных форм первичной учетной документации при осуществлении
        перевозок пассажиров и багажа автомобильным транспортом;<br />
        <br />
        б) разрешения на автомобильную перевозку пассажиров по регулярному
        маршруту (рейсу) и расписания движения по маршруту (рейсу).<br />
        <br />
        155. Водители транспортных средств без предъявления документов,
        предусмотренных пунктом 158 настоящих Правил, к обслуживанию оператором
        автомобильных перевозок не подлежат, за исключением случаев, указанных в
        настоящих Правилах.<br />
        <br />
        156. Операторы автомобильных перевозок обеспечивают централизованное
        диспетчерское сопровождение на регулярных маршрутах (рейсах)
        автомобильных перевозок пассажиров и багажа в соответствии с
        требованиями действующего законодательства Приднестровской Молдавской
        Республики.<br />
        <br />
        157. В случае, если начальным пунктом регулярного маршрута является
        населенный пункт, в котором отсутствует автовокзал, автостанция или
        диспетчерско-кассовый пункт, а далее данный регулярный маршрут пролегает
        через населенный пункт, в котором расположен объект транспортной
        инфраструктуры, является обязательным заезд автобуса на автовокзал,
        автостанцию или диспетчерско-кассовый пункт с оформлением
        соответствующих унифицированных форм первичной учетной документации в
        порядке, установленном действующим законодательством Приднестровской
        Молдавской Республики.<br />
        <br />
        В случае, если регулярный маршрут (рейс) пролегает через населенный
        пункт, в котором расположен объект транспортной инфраструктуры или
        остановочный пункт, то в расписании движения регулярного пригородного,
        междугородного, международного сообщения в обязательном порядке
        указывается заезд автобусов на автовокзал, автостанцию или
        диспетчерско-кассовый пункт.<br />
        <br />
        158. В случае, если начальным пунктом отправления регулярного
        международного маршрута (рейса) является населенный пункт
        Приднестровской Молдавской Республики, то иностранный перевозчик обязан
        заключить договор со специализированной организацией, осуществляющей
        предрейсовое медицинское освидетельствование водителей и технический
        осмотр транспортных средств.<br />
        <br />
        159. Оператор автомобильных перевозок ежемесячно, в срок до 10 числа,
        представляет в исполнительный орган государственной власти, в ведении
        которого находятся вопросы управления транспортом, отчет по срывам и не
        заездам перевозчиков, обслуживающих регулярные маршруты, отчет об
        объемах услуг пассажирского автомобильного транспорта, оказанных
        операторами автомобильных перевозок (автовокзалами, автостанциями и
        диспетчерско-кассовыми пунктами) по формам, установленным исполнительным
        органом государственной власти, к ведению которого отнесены вопросы
        управления транспортом.<br />
        <br />
        160. Оператор автомобильных перевозок обязан расторгнуть договор с
        перевозчиком при расторжении исполнительным органом государственной
        власти, к ведению которого отнесены вопросы управления транспортом,
        договор об обслуживании регулярных маршрутов (рейсов) с перевозчиком.
        Расторжение оператором автомобильных перевозок договора с перевозчиком,
        допущенным в установленном законодательством Приднестровской Молдавской
        Республики порядке к осуществлению данного вида деятельности, без
        согласования c исполнительным органом государственной власти, к ведению
        которого отнесены вопросы управления транспортом, не допускается.<br />
        <br />
        161. В случае увеличения пассажиропотока на регулярных маршрутах
        оператор автомобильных перевозок вправе привлекать к выполнению
        дополнительных рейсов на регулярных маршрутах перевозчиков при
        соблюдении следующих условий:<br />
        <br />
        а) минимальный коэффициент загрузки автомобильного транспорта общего
        пользования должен быть не менее 0,8 из начального пункта
        отправления;<br />
        <br />
        б) интервал движения между дополнительным рейсом и последующим основным
        в данном направлении должен составлять не менее 10 минут при условии,
        что коэффициент загрузки предыдущего и последующего рейсов будут не
        менее 1,0.<br />
        <br />
        162. При привлечении перевозчиков для выполнения дополнительных рейсов
        приоритет отдается перевозчику, обслуживающему данный регулярный
        маршрут.<br />
        <br />
        163. При организации дополнительных рейсов оператор автомобильных
        перевозок обязан:<br />
        <br />
        а) объявить пассажирам через средства громкоговорящей связи о начале
        продажи проездных билетов на автобус, выполняющий дополнительный рейс по
        соответствующему регулярному маршруту;<br />
        <br />
        б) внести соответствующую запись "Дополнительный рейс по регулярному
        маршруту" (указывается наименование маршрута) в путевой лист,
        билетно-учетный лист и диспетчерский журнал (прошитый в установленном
        порядке) с обязательным указанием времени отправления автобуса, фамилии,
        имени, отчества диспетчера автовокзала (автостанции) и проставлением в
        данных документах штампа автовокзала (автостанции).
