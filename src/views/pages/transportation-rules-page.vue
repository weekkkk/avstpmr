      <h3 class="medium is-adaptive">
        3. Требования к водителям транспортных средств
      </h3>
      <p class="regular is-adaptive align-j">
        25. К выполнению городских, пригородных и междугородных автомобильных
        перевозок пассажиров автобусами допускаются водители, имеющие
        водительское удостоверение на право управления транспортными средствами
        соответствующей категории.<br />
        <br />
        26. К выполнению международных автомобильных перевозок пассажиров
        автобусами допускаются водители, имеющие общий стаж работы не менее 3
        (трех) лет в качестве водителя.<br />
        <br />
        27. Водители должны знать требования нормативных правовых актов,
        регламентирующих выполняемые ими автомобильные перевозки пассажиров.<br />
        <br />
        28. Стаж работы в качестве водителя автомобиля подтверждается при приеме
        его на работу записями в трудовой книжке, а при необходимости -справками
        с предыдущих мест работы, заверенными копиями приказов или договоров
        (контрактов).<br />
        <br />
        29. Водитель допускается к выполнению перевозок пассажиров при наличии
        медицинской справки о допуске к управлению транспортными средствами и
        медицинской книжки установленного образца, а также ежедневного
        предрейсового медицинского освидетельствования перед началом работы, о
        чем должна быть произведена отметка в путевом листе.<br />
        <br />
        30. Водитель, выполняющий автомобильные перевозки пассажиров, должен
        иметь и представлять для проверки по требованию контролирующих лиц
        следующие документы:<br />
        <br />
        а) водительское удостоверение соответствующей категории;<br />
        <br />
        б) свидетельство о регистрации транспортного средства (технический
        паспорт автомобиля);<br />
        <br />
        в) документ, подтверждающий прохождение государственного технического
        осмотра;<br />
        <br />
        г) разрешение на автомобильную перевозку пассажиров по регулярному
        маршруту.<br />
        <br />
        д) билетно-учетный лист (за исключением городских перевозок);<br />
        <br />
        е) путевой лист установленной формы с отметкой о прохождении ежедневного
        предрейсового медицинского и технического осмотра транспортного
        средства;<br />
        <br />
        ж) регистрационные листки (тахограммы, распечатки) в необходимом
        количестве, если транспортное средство оборудовано контрольным
        устройством (тахографом);<br />
        <br />
        з) удостоверение о прохождении водителем обучения безопасной работе с
        газобаллонными транспортными средствами и акты о прохождении
        освидетельствования баллонов и о переоборудовании транспортного средства
        для работы на газовом топливе (при выполнении перевозок на транспортном
        средстве, работающем на сжатом или сжиженном газе);<br />
        <br />
        е) ксерокопия лицензии на осуществление международных перевозок.<br />
        <br />
        и) документ, подтверждающий соответствие требованиям, предъявляемым к
