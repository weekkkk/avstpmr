    <div class="block">
      <h3 class="medium is-adaptive">21. Общие положения</h3>
      <p class="regular is-adaptive align-j">
        258. Нерегулярные (заказные) автомобильные перевозки пассажиров и багажа
        осуществляются по дорожной сети и дорожным сооружениям, движение по
        которым разрешено для соответствующих автотранспортных средств.<br />
        <br />
        К нерегулярным (заказным) перевозкам относятся разовые перевозки
        организованной группы пассажиров по определенному маршруту
        (туристическому, экскурсионному, по обслуживанию мероприятий, а также
        разовые перевозки к местам отдыха и другие).<br />
        <br />
        Нерегулярные перевозки (разовые) пассажиров осуществляются в каждом
        конкретном случае на основе договора между перевозчиком и юридическим
        или физическим лицом (заявки). Условия перевозки, включая условия
        оплаты, определяются в каждом конкретном случае в заявках или
        договорах.<br />
        <br />
        Исполнительный орган государственной власти, в ведении которого
        находятся вопросы управления транспортом, ведет реестр перевозчиков,
        осуществляющих нерегулярные (заказные) перевозки пассажиров и багажа,
        который ежегодно публикуется в средствах массовой информации.<br />
        <br />
        Основаниями для включения в реестр является соответствие перевозчика
        обязательным требованиям, установленным пунктом 264 настоящих Правил.<br />
        <br />
        Осуществление перевозчиками нерегулярных (заказных) перевозок пассажиров
        и багажа, не включенными исполнительным органом государственной власти,
        в ведении которого находятся вопросы управления транспортом в реестр,
        запрещается.<br />
        <br />
        Основания для исключения из реестра являются выявление неоднократных
        (более двух) нарушений требований и условий выполнения нерегулярных
        (заказных) перевозок пассажиров и багажа, установленных настоящими
        Правилами.<br />
        <br />
        259. Нерегулярные (заказные) перевозки пассажиров автобусами выполняются
        на основе контрольных ведомостей (списка пассажиров), выдаваемых
        исполнительным органом государственной власти, в ведении которого
        находятся вопросы управления транспортом, на каждый отдельный рейс.<br />
        <br />
        260. К нерегулярным (заказным) автомобильным перевозкам пассажиров и
        багажа не относятся и подлежат выполнению в соответствии с требованиями,
        установленными в отношении регулярных перевозок:<br />
        <br />
        а) осуществление с целью извлечения прибыли перевозок одним перевозчиком
        или одним транспортным средством по одному маршруту или между одними и
        теми же пунктами;<br />
        <br />
        б) осуществление с целью извлечения прибыли перевозок по заранее
        определенным направлениям, предоставление услуг по перевозке
        транспортным средством по заранее определенному маршруту, между
        определенными конечными пунктами, с установленным временем отправления,
        прибытия и стоимостью проезда;<br />
        <br />
        в) перевозка с целью извлечения прибыли пассажиров с остановочных
        пунктов, предусмотренных для регулярных маршрутов (рейсов), либо по пути
        следования регулярного маршрута.<br />
        <br />
        262. При организации нерегулярных (заказных) автомобильных перевозок
        пассажиров и багажа перевозчик обеспечивает наличие в транспортном
        средстве:<br />
        <br />
        а) путевого листа с отметками о прохождении медицинского
        освидетельствования водителя и технического осмотра транспортного
        средства;<br />
        <br />
        б) контрольной ведомости (список пассажиров).<br />
        <br />
        263. Форма, порядок заполнения документов на осуществление нерегулярных
        (заказных) перевозок устанавливается исполнительным органом
        государственной власти, в ведении которого находятся вопросы управления
        транспортом.<br />
        <br />
        264. Нерегулярные (заказные) перевозки пассажиров и багажа
        осуществляются транспортными средствами, соответствующими требованиям
        настоящих Правил. Число пассажиров, перевозимых в транспортном средстве
        в нерегулярном сообщении, не должно превышать числа мест, оборудованных
        для сидения, в соответствии со свидетельством о регистрации
        транспортного средства.<br />
        <br />
        К обязательным требованиям, предъявляемым к перевозчикам, осуществляющим
        нерегулярные (заказные) автомобильные перевозки пассажиров и багажа,
        относятся:<br />
        <br />
        а) соблюдение перевозчиком установленных законами и иными нормативными
        правовыми актами Приднестровской Молдавской Республики в области
        автомобильного транспорта требований по организации и осуществлению
        перевозок пассажиров и багажа;<br />
        <br />
        б) наличие у перевозчика на праве собственности или на ином законном
        основании транспортных средств, соответствующих требованиям
        безопасности;<br />
        <br />
        в) наличие у перевозчика должностного лица (менеджера), ответственного
        за обеспечение безопасности дорожного движения и организацию
        автомобильных перевозок пассажиров (при осуществлении международных
        автомобильных перевозок);<br />
        <br />
        г) наличие у перевозчика водителей на основании заключенных трудовых
        договоров, имеющих водительские удостоверения соответствующих
        категорий;<br />
        <br />
        д) наличие у перевозчика специалистов, а также помещений и оборудования,
        принадлежащих перевозчику на праве собственности или на ином законном
        основании, позволяющих осуществлять предрейсовое медицинское
        освидетельствование водителей, или договор со специализированной
        организацией, осуществляющей предрейсовое медицинское
        освидетельствование водителей;<br />
        <br />
        е) наличие у перевозчика специалистов, а также помещений и оборудования,
        принадлежащих перевозчику на праве собственности или на ином законном
        основании, позволяющих осуществлять хранение, техническое обслуживание и
        ремонт транспортных средств, или договоров со специализированными
        организациями на техническое обслуживание и ремонт транспортных
        средств;<br />
        <br />
        ж) наличие у перевозчика специалистов, а также помещений и оборудования,
        принадлежащих перевозчику на праве собственности или на ином законном
        основании, позволяющих осуществлять предрейсовый технический осмотр
        транспортных средств, или договор со специализированной организацией,
        осуществляющей технический осмотр транспортных средств;<br />
        <br />
        з) наличие действующей лицензии, если законодательством Приднестровской
        Молдавской Республики данный вид деятельности подлежит
        лицензированию.<br />
        <br />
        265. Тарифы на перевозку пассажиров в нерегулярном сообщении
        устанавливаются перевозчиками.<br />
        <br />
        266. Автомобильные перевозки пассажиров в нерегулярном сообщении с
        территории автовокзалов, автостанций, диспетчерско-кассовых пунктов
        запрещаются.<br />
        <br />
        267. Остановочные пункты при осуществлении нерегулярных (заказных)
        автомобильных перевозок пассажиров и багажа должны обеспечивать
        безопасную посадку пассажиров.<br />
        <br />
        268. Оплата проезда и провоза багажа при нерегулярных (заказных)
        автомобильных перевозках пассажиров и багажа производится пассажирами
        перевозчику (водителю) в соответствии с условиями договора.<br />
        <br />
        269. При осуществлении нерегулярных (заказных) автомобильных перевозок
        пассажиров и багажа запрещается самовольное (не предусмотренное
        условиями договора) определение начальных, промежуточных и конечных
        пунктов перевозки.<br />
        <br />
        270. На передний трафарет автобуса должна быть помещена надпись
        "Заказной.<br />
        <br />
        271. При проведении нерегулярных (заказных) перевозок водитель должен
        иметь при себе путевой лист, список пассажиров (контрольную
        ведомость).<br />
        <br />
        272. Контрольные ведомости (список пассажиров) установленного образца
        являются документом строгой отчетности, оформляются на каждую поездку с
        внесением фамилий перевозимых пассажиров, основных данных прилагаемых
        платежных документов и других необходимых сведений. После выполнения
        перевозок копии контрольных ведомостей (списка пассажиров) направляются
        уполномоченному органу, их выдавшему.<br />
        <br />
        273. Контрольные ведомости (список пассажиров) в копиях или произвольной
        форме, без печатей уполномоченного органа, выдавшего их, считаются
        недействительными и подлежат изъятию.
      </p>
