      <h3 class="medium is-adaptive">
        14. Оплата проезда и провоза багажа в регулярном сообщении
      </h3>
      <p class="regular is-adaptive align-j">
        164. На регулярных городских маршрутах разрешается провозить в салоне
        транспортного средства без дополнительной оплаты одно место ручной клади
        размером 60х40х20 см и весом не более 10 (десяти) кг, если багаж не
        мешает свободному перемещению пассажиров по салону автобуса.<br />
        <br />
        165. На регулярных пригородных, междугородных и международных маршрутах
        в салоне автобуса пассажирам разрешается перевозить бесплатно одно место
        ручной клади размером не более 60х40х20 см и весом не более 15
        (пятнадцати) кг, а также мелких животных и птиц в клетках. Не
        допускаются к перевозке в автобусах предметы и вещи габаритом более
        100х50х30 см или загрязняющие подвижной состав или одежду пассажиров.
        Обеспечение целостности и сохранности ручной клади является обязанностью
        пассажира. Размещение ручной клади на местах, предназначенных для
        сидения, в проходе между сиденьями, возле входа или выхода из
        транспортного средства, в том числе аварийного, запрещается.<br />
        <br />
        166. Пассажирам также разрешается провозить с собой багаж в багажном
        отделении автобуса за дополнительную плату в соответствии с тарифом
        каждого багажного места, определенным в соответствии с методикой расчета
        тарифов на соответствующие регулярные перевозки пассажиров и багажа и
        утвержденным действующим законодательством Приднестровской Молдавской
        Республики.<br />
        <br />
        167. В автобусах, осуществляющих перевозку пассажиров на пригородных,
        междугородных и международных маршрутах, имеющих багажное отделение
        (отсек), пассажир имеет право провозить с собой за плату два места
        багажа размером не более 100х50х30 см.<br />
        <br />
        168. Упаковка и перевозка ручной клади должны исключать возможность
        причинения какого-либо ущерба (загрязнения или повреждения) другим
        пассажирам и оснащению салона транспортного средства.<br />
        <br />
        169. Сдаваемый к перевозке багаж должен быть в исправной упаковке. На
        принятый к перевозке багаж пассажиру выдается багажная квитанция.
        Погрузка и выгрузка багажа, перевозимого в багажных отделениях
        транспортных средств, осуществляется пассажиром.<br />
        <br />
        170. При сдаче багажа к перевозке пассажир может объявить его ценность.
        При этом в багажной квитанции делается соответствующая запись.<br />
        <br />
        171. При обнаружении утери, недостачи или повреждения (порчи) багажа
        составляется два экземпляра акта об утере, недостачи или повреждения
        багажа с участием владельца багажа, представителя перевозчика или
        водителя и сотрудника правоохранительных органов. Один экземпляр акта
        вручается владельцу багажа.<br />
        <br />
        172. В акте об утере, недостачи или повреждении багажа указывается
        фамилия, имя, отчество и адрес владельца, количество принятых и выданных
        мест, наименование утерянного предмета или характер его повреждения,
        сумма объявленной ценности, если она была оформлена документально.<br />
        <br />
        173. Багаж, принятый к перевозке, считается утраченным, если этот багаж
        не прибыл в пункт назначения перевозки в течение 3 (трех) дней по
        истечении срока доставки.<br />
        <br />
        174. Перевозчик несет ответственность за утрату, недостачу и повреждение
        (порчу) багажа в соответствии с действующим законодательством
        Приднестровской Молдавской Республики.<br />
        <br />
        175. На регулярных городских маршрутах разрешается бесплатно перевозить
        мелких животных и птиц, помещенных в закрытые клетки или в сумки, если
        они не превышают установленных настоящими Правилами пределов ручной
        клади.<br />
        <br />
        176. Собаки могут перевозиться в транспортных средствах с намордником и
        на поводке.<br />
        <br />
        177. В автобусах, используемых при осуществлении регулярных пригородных,
        междугородных, международных автомобильных перевозок пассажиров и багажа
        в экспрессном режиме и имеющих багажное отделение, пассажирам
        разрешается провозить с собой багаж в багажном отделении размером не
        более 120х60х40 см каждого места за дополнительную плату в соответствии
        с установленным тарифом.<br />
        <br />
        Исключен(-а)<br />
        <br />
        178. Оплата проезда и провоза багажа на регулярных городских маршрутах
        производится пассажиром кондуктору (водителю) при посадке в транспортное
        средство.<br />
        <br />
        179. Оплата проезда и провоза багажа по регулярным пригородным,
        междугородным и международным маршрутам производится пассажирами в кассу
        автовокзала, автостанции, диспетчерско-кассового пункта, а на
        промежуточных остановочных пунктах маршрутов - кондуктору или водителю
        непосредственно при посадке в автобус до его отправления от
        остановочного пункта. Факт продажи билета фиксируется кассиром в
        посадочной ведомости или водителем в билетно-учетном листе. Формы
        проездных документов (билетов) утверждаются исполнительным органом
        государственной власти, к ведению которого отнесены вопросы управления
        транспортом.<br />
        <br />
        180. Запрещается осуществлять продажу билетов во время движения, а также
        в местах, где имеются билетные кассы.<br />
        <br />
        181. На автовокзалах, автостанциях, диспетчерско-кассовых пунктах
        проверку наличия проездного документа у пассажира осуществляет диспетчер
        оператора автомобильных перевозок, а на промежуточных остановочных
        пунктах - водители или контролеры.<br />
        <br />
        182. Проездной билет действителен только на автобус того рейса, номер
        которого и дата отправления указаны в нем. Билеты без указания станции
        назначения с неверными и исправленными названиями автовокзалов или
        автостанций и другими неточностями считаются недействительными.<br />
        <br />
        183. Каждый пассажир обязан иметь билет на проезд в автобусе и багажную
        квитанцию (кроме городских перевозок) на провоз багажа.<br />
        <br />
        184. Пассажиры сохраняют проездные документы (билеты) в течение всего
        времени поездки и предъявляют для проверки по первому требованию лиц,
        уполномоченных осуществлять контроль за их наличием, в соответствии с
        действующим законодательством Приднестровской Молдавской Республики.<br />
        <br />
        185. Пассажир занимает место в автобусе согласно указанному в билете
        номеру, за исключением городских перевозок.<br />
        <br />
        186. При выполнении автомобильных перевозок пассажиров в регулярном
        сообщении может применяться электронный билет, в котором информация об
        автомобильной перевозке пассажира представляется в электронно-цифровой
        форме.<br />
        <br />
        187. Реализация электронных билетов осуществляется посредством
        информационных систем, позволяющих обеспечить прямой доступ к базе
        данных расписаний движения автобусов по маршрутам, наличия свободных
        мест и иной информации об условиях автомобильной перевозки в регулярном
        сообщении. Порядок использования автоматизированной системы оплаты
        проезда (АСОП) на регулярных городских, пригородных и междугородных
        маршрутах (рейсах) устанавливается Правительством Приднестровской
        Молдавской Республики.<br />
        <br />
        188. При оформлении электронного билета ему автоматически присваивается
        номер, по которому осуществляется учет использования данного
        электронного билета.<br />
        <br />
        189. Порядок использования билетов, установленный настоящими Правилами,
        применяется к порядку использования электронных билетов, если иное не
        вытекает из существа этих правоотношений.<br />
        <br />
        190. Факт оплаты провоза багажа подтверждается выдачей пассажиру
        багажной квитанции.<br />
        <br />
        191. Если билет приобретается пассажиром в пути следования на
        промежуточных остановочных пунктах пригородного, междугороднего и
        международного маршрута, расположенных вне автовокзала, автостанции,
        диспетчерско-кассового пункта, то кондуктор или водитель проставляет в
        билете начальный и конечный пункты следования пассажира, оплаченную
        стоимость проезда, дату и время отправления, а также делает
        соответствующую запись в билетно-учетном листе.<br />
        <br />
        192. Порядок оформления путевого и билетно-учетного листа и их формы
        утверждаются исполнительным органом государственной власти, к ведению
        которого отнесены вопросы управления транспортом, в пределах
        компетенции, установленной законодательством Приднестровской Молдавской
        Республики.<br />
        <br />
        193. Тарифы на городские перевозки пассажиров и провоз багажа
        устанавливаются в соответствии с действующим законодательством
        Приднестровской Молдавской Республики по согласованию с органами
        местного государственного управления.<br />
        <br />
        194. Уведомление населения о предстоящем изменении тарифов на регулярных
        городских маршрутах должно осуществляться через средства массовой
        информации не позднее 15 (пятнадцати) дней до введения новых тарифов.<br />
        <br />
        195. В случае возникновения обстоятельств, при которых транспортное
        средство не может продолжать движение по регулярному городскому,
        пригородному, междугородному и международному маршруту (рейсу) по
        техническим причинам, выданные пассажирам билеты являются
        действительными для проезда на другом транспортном средстве этого же
        маршрута.<br />
        <br />
        В случае возникновения обстоятельств, при которых транспортное средство
        не может продолжать движение по регулярному городскому маршруту (рейсу)
        по техническим причинам, стоимость проезда возвращается в полном
        объеме.<br />
        <br />
        196. При невозможности доставки пассажира до пункта назначения по
        дорожно-климатическим и другим причинам, не зависящим от перевозчика,
        пассажиру в течение 1 (одного) дня со времени отмены рейса регулярного
        пригородного, междугородного и международного маршрута перевозчиком
        возвращается стоимость проезда от пункта прекращения поездки до места
        назначения.<br />
        <br />
        197. При возврате автобуса в начальный пункт следования регулярного
        пригородного, междугородного или международного маршрута пассажиру
        возвращается стоимость проезда и провоза ручной клади.<br />
        <br />
        198. При отсутствии возможности реализации проездных билетов посредством
        электронных устройств допускается продажа билетов установленной формы
        заполненных вручную.
