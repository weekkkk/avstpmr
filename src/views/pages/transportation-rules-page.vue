      <h3 class="medium is-adaptive">
        16. Требования к оснащению и размещению остановочных пунктов
      </h3>
      <p class="regular is-adaptive align-j">
        212. Остановочные пункты регулярных маршрутов (рейсов) размещаются с
        учетом безопасного и удобного подхода пассажиров при соблюдении правил
        дорожного движения, строительных норм и правил, а также правил
        технической эксплуатации таких пунктов.<br />
        <br />
        213. Каждый остановочный пункт оснащается информационным указателем с
        наименованием остановочного пункта, номерами проходящих маршрутов,
        интервалом движения по маршрутам (рейсам).<br />
        <br />
        214. Исполнительные органы государственной власти и органы местного
        государственного управления обеспечивают ежегодный ремонт остановочных
        пунктов в период весенней санитарной очистки и благоустройства
        населенных пунктов.<br />
        <br />
        215. Остановочные пункты регулярных маршрутов (кроме городских
        маршрутов) должны располагаться в каждом населенном пункте по пути
        следования маршрута в местах, наиболее удобных для жителей этих
        населенных пунктов, быть по возможности приближенными к крупным
        пассажирообразующим центрам (железнодорожным вокзалам, рынкам) и могут
        совмещаться с остановочными пунктами регулярных городских маршрутов.<br />
        <br />
        216. При наличии в населенных пунктах по пути следования регулярного
        пригородного, междугородного или международного маршрута автовокзала,
        автостанции, диспетчерско-кассового пункта маршрут должен проходить
        через этот автовокзал, автостанцию, диспетчерско-кассовые пункты.
