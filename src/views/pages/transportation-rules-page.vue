      <h3 class="medium is-adaptive">2. Требования к транспортным средствам</h3>
      <p class="regular is-adaptive align-j">
        11. К автомобильным перевозкам пассажиров и багажа на регулярных
        маршрутах допускаются транспортные средства, соответствующие
        установленным техническим требованиям, прошедшие государственный
        технический осмотр и включенные исполнительным органом государственной
        власти, к ведению которого отнесены вопросы управления транспортом
        (органом местного государственного управления), в реестр подвижного
        состава.<br />
        <br />
        Реестр подвижного состава ведется исполнительным органом государственной
        власти, к ведению которого отнесены вопросы управления транспортом
        (орган местного государственного управления).<br />
        <br />
        Включение транспортного средства в реестр подвижного состава
        осуществляется в заявительном порядке на основании поданных документов,
        подтверждающих право пользования транспортным средством.<br />
        <br />
        12. Выполнение нерегулярных (заказных) автомобильных перевозок
        пассажиров автобусами без наличия сертификата соответствия транспортного
        средства, изготовленного после 1994 года, не допускается. Порядок выдачи
        сертификатов соответствия транспортных средств устанавливается
        исполнительным органом государственной власти, к ведению которого
        отнесены вопросы технического регулирования и обеспечения единства
        измерениям.<br />
        <br />
        13. Допускается на городских и пригородных маршрутах перевозка не более
        5 стоящих пассажиров в час пик при условии соблюдения требований
        безопасности и оборудования салона автобуса поручнями и ручками в
        соответствии с требованиями ГОСТ 27815-88 "Автобусы. Общие требования к
        безопасности конструкции", введенного в действие Приказом Министерства
        промышленности Приднестровской Молдавской Республики № 474 от 28 ноября
        2002 года (регистрационный № 1901 от 11 декабря 2002 года) (САЗ
        02-50).<br />
        <br />
        14. Автобусы, обслуживающие международные пассажирские перевозки с
        протяженностью маршрута (рейса) более 300 км в одном направлении,
        оборудуются тахографами.<br />
        <br />
        15. Порядок использования тахографов в Приднестровской Молдавской
        Республике устанавливается исполнительным органом государственной
        власти, к ведению которого отнесены вопросы управления транспортом.<br />
        <br />
        16. Транспортные средства, используемые при автомобильных перевозках
        пассажиров, должны комплектоваться:<br />
        <br />
        а) медицинской аптечкой с необходимым набором лекарств и медицинских
        средств;<br />
        <br />
        б) стояночным средством (башмаками) для фиксации от самопроизвольного
        движения при стоянке на дороге с уклоном (для автобусов);<br />
        <br />
        в) знаком аварийной остановки;<br />
        <br />
        г) средствами пожаротушения;<br />
        <br />
        д) информационными табличками в салоне транспортных средств о способах
        экстренного открывания аварийных люков, дверей, окон и удаления стекол в
        случае дорожно-транспортного происшествия (для автобусов);<br />
        <br />
        е) противосолнечные приспособления на окнах (шторы или затемнение
        стекол);<br />
        <br />
        ж) тахографом (при организации международных перевозок пассажиров и
        багажа с протяженностью маршрута (рейса) более 300 км в одном
        направлении).<br />
        <br />
        17. Автобусы, используемые при регулярных автомобильных перевозках
        пассажиров и багажа, должны иметь:<br />
        <br />
        а) двери пассажирского салона и аварийные люки, открывающиеся и
        закрывающиеся без каких-либо помех. Двери не должны иметь острых или
        далеко отстоящих от их поверхности выступов;<br />
        <br />
        б) крышу, аварийные люки и окна, которые в закрытом состоянии полностью
        предотвращают попадание атмосферных осадков в кабину водителя и
        пассажирский салон;<br />
        <br />
        в) прочно закрепленные поручни и сидения;<br />
        <br />
        г) чистые и без порывов обшивки сидений и спинок кресел для
        пассажиров;<br />
        <br />
        д) ровные, без выступающих или незакрепленных деталей, подножки и пол
        салона;<br />
        <br />
        е) покрытие пола салона должно быть выполнено из сплошного материала без
        порывов;<br />
        <br />
        ж) пассажирский салон, отапливаемый в холодное и вентилируемый в жаркое
        время года, не загроможденный инструментом и запасными частями.<br />
        <br />
        Влажная уборка салонов автобусов, используемых на регулярных маршрутах,
        проводится по мере загрязнения, но не менее двух раз в смену. Наружная
        мойка кузова проводится после окончания смены.<br />
        <br />
        18. В салоне автобусов, используемых при регулярных городских,
        пригородных, междугородных, международных автомобильных перевозках
        пассажиров и багажа, должна располагаться следующая информация:<br />
        <br />
        а) полное наименование перевозчика;<br />
        <br />
        б) номера контактных телефонов перевозчика;<br />
        <br />
        в) фамилия, имя, отчество (при наличии) водителя автобуса.<br />
        <br />
        г) установленный тариф на проезд по маршруту (рейсу) и провоз багажа.
        Для регулярных маршрутов (кроме городских маршрутов) должна быть
        представлена сетка тарифов в зависимости от расстояния поездки;<br />
        <br />
        д) выписка из настоящих Правил с указанием прав и обязанностей водителя
        и пассажира.<br />
        <br />
        е) указатели номеров сидений (кроме городских и пригородных
        маршрутов).<br />
        <br />
        ж) расписание и схема движения по регулярному маршруту (рейсу), интервал
        и режим работы для городских маршрутов;<br />
        <br />
        з) информационные таблички с указанием аварийного выхода, аптечки и
        огнетушителя.<br />
        <br />
        19. На передней и задней сторонах автобусов, используемых при регулярных
        городских автомобильных перевозках пассажиров и багажа, устанавливаются
        трафареты с указанием номера, начального и конечного пункта маршрута,
        который должен быть размером 500х200 мм на белом фоне.<br />
        <br />
        20. С правой стороны автобусов, используемых на городских регулярных
        маршрутах, возле одной из пассажирских дверей салона должен помещаться
        трафарет с номером и кратким описанием маршрута (наименования
        промежуточных и конечных остановок, основных улиц, по которым проходит
        маршрут, общественно значимых мест).<br />
        <br />
        21. Надписи на трафаретах междугородного маршрута должны быть написаны
        зеленым цветом (высота шрифта не менее 120 мм) на белом фоне.<br />
        <br />
        22. Надписи на трафаретах пригородного маршрута должны быть написаны
        синим цветом (высота шрифта не менее 120 мм) на белом фоне.<br />
        <br />
        23. Передние трафареты должны иметь подсветку в темное время суток.<br />
        <br />
        24. В передней части салона автобусов, используемых при регулярных
        автомобильных перевозках пассажиров и багажа, предусматриваются места
        для инвалидов, пенсионеров, беременных женщин и пассажиров с детьми
        дошкольного возраста, оборудованные информационной табличкой, содержащей
        следующую информацию: "Места для инвалидов" "Места для пассажиров с
        детьми дошкольного возраста".
