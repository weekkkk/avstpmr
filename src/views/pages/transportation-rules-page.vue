      <h3 class="medium is-adaptive">
        15. Порядок обследования и ведения реестра регулярных маршрутов
      </h3>
      <p class="regular is-adaptive align-j">
        199. Вновь открываемые регулярные маршруты подлежат обследованию с целью
        оценки соответствия технического состояния и уровня содержания
        автодорожной инфраструктуры требованиям безопасности пассажирских
        перевозок.<br />
        <br />
        200. Обследование перед открытием нового регулярного городского,
        пригородного или междугородного маршрута либо перед изменением схемы
        движения на существующем регулярном маршруте проводит комиссия,
        создаваемая исполнительным органом государственной власти, к ведению
        которого отнесены вопросы управления транспортом (органом местного
        государственного управления).<br />
        <br />
        201. В этом случае обследование проводится с привлечением представителей
        дорожных организаций, Государственной автомобильной инспекции
        Министерства внутренних дел Приднестровской Молдавской Республики и при
        необходимости с участием объединений перевозчиков.<br />
        <br />
        202. В случае изменения схемы движения на существующем регулярном
        маршруте обследованию подлежит участок, подлежащий изменению.<br />
        <br />
        203. Обследование перед открытием нового рейса на существующем
        регулярном маршруте не проводится.<br />
        <br />
        204. Результат каждого обследования регулярного маршрута оформляется
        актом, в котором дается заключение о возможности открытия нового
        регулярного маршрута, изменения схемы движения на существующем
        регулярном маршруте.<br />
        <br />
        205. В случае выявления несоответствия требованиям безопасности
        перевозок в акте отражаются предложения о проведении неотложных и
        перспективных мероприятий, направленных на улучшение условий движения и
        предупреждение дорожно-транспортных происшествий на регулярном
        маршруте.<br />
        <br />
        206. Акты обследования передаются в исполнительный орган государственной
        власти, в ведении которого находятся вопросы управления транспортом
        (орган местного государственного управления), для решения вопроса об
        открытии нового регулярного маршрута, об изменении схемы движения на
        существующем регулярном маршруте, оформления или изменения паспорта
        регулярного маршрута, принятия мер по повышению безопасности
        автомобильных перевозок, организации контроля за устранением выявленных
        недостатков.<br />
        <br />
        207. Исполнительный орган государственной власти, в ведении которого
        находятся вопросы управления транспортом (орган местного
        государственного управления), вправе проверить достоверность сведений,
        указанных в соответствующих актах обследования, либо затребовать
        дополнительную информацию.<br />
        <br />
        208. Копии актов направляются в дорожные организации, в ведении которых
        находятся объекты автодорожной инфраструктуры, для устранения выявленных
        недостатков, а также перевозчику, осуществляющему перевозки на
        обследованном маршруте, для использования при проведении инструктажей
        водителей, уточнения схем опасных участков, нормирования скоростей
        движения транспортных средств.<br />
        <br />
        209. В случае несоответствия автодорожной инфраструктуры требованиям
        безопасности перевозок на основании актов обследования маршрутов
        исполнительным органом государственной власти, к ведению которого
        отнесены вопросы управления транспортом (органом местного
        государственного управления), принимается решение об отказе в открытии
        нового, об изменении или закрытии действующего регулярного маршрута.<br />
        <br />
        210. Исполнительным органом государственной власти, к ведению которого
        отнесены вопросы управления транспортом, и органами местного
        государственного управления оборудуются остановочные пункты
        информационными стендами с указанием времени отправления автобусов
        (расписания движения) перевозчиков с данного остановочного пункта. Для
        городских маршрутных такси указываются номера маршрутов, интервалы
        движения и время работы маршрутов.<br />
        <br />
        211. В целях планирования организации и учета регулярных маршрутов
        (рейсов) осуществляется ведение реестра:<br />
        <br />
        а) исполнительным органом государственной власти, к ведению которого
        отнесены вопросы управления транспортом, - регулярных маршрутов (рейсов)
        пригородных, междугородных и международных автомобильных перевозок
        пассажиров и багажа;<br />
        <br />
        б) органами местного государственного управления - регулярных маршрутов
        (рейсов) городских автомобильных перевозок пассажиров и багажа.<br />
        <br />
        Разрешения на автомобильную перевозку пассажиров по регулярному
        пригородному, междугородному, международному маршруту, включенным в
        реестр регулярных маршрутов, выдаются исполнительным органом
        государственной власти, к ведению которого отнесены вопросы управления
        транспортом, перевозчикам на период, указанный в реестре регулярных
        маршрутов.<br />
        <br />
        В реестр регулярных маршрутов вносятся изменения ежегодно только в
        период с 1 января по 28 февраля в следующих случаях:<br />
        <br />
        a) открытие новых регулярных маршрутов (рейсов);<br />
        <br />
        б) изменения расписания движения регулярных маршрутов (рейсов);<br />
        <br />
        в) включения, исключения или смены автовокзалов и/или остановочных
        пунктов;<br />
        <br />
        г) изменения периодичности обслуживания регулярного маршрута (рейса);<br />
        <br />
        д) проведения конкурса регулярных маршрутов (рейсов).<br />
        <br />
        В случаях, предусмотренных подпунктом "д" пункта 139, подпунктами "а",
        "д" пункта 140 настоящих Правил, а также при открытии нового регулярного
        международного маршрута или новых рейсов на существующем регулярном
        международном маршруте, если перевозчик представил документы в полном
        объеме в соответствии с требованиями настоящих Правил, изменение реестра
        регулярных маршрутов может осуществляться ежеквартально.<br />
        <br />
        Заявки от перевозчиков об открытии регулярного пригородного,
        междугородного маршрута (рейса) или изменении расписания движения
        принимаются исполнительным органом государственной власти, к ведению
        которого отнесены вопросы управления транспортом (органами местного
        государственного управления), в период с 1 октября до 1 декабря
        ежегодно. В другой временной период заявки от перевозчиков к
        рассмотрению не принимаются.
