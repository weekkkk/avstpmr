        <br />
        я-1) паспорт регулярного маршрута - оформленный и утвержденный
        исполнительным органом государственной власти, к ведению которого
        отнесены вопросы управления транспортом (органом местного
        государственного управления), документ, характеризующий маршрут, в
        котором указываются: наличие линейных сооружений, остановочных пунктов с
        указанием адреса (в населенных пунктах), состояние дороги, а также схема
        движения по регулярному маршруту.<br />
        <br />
        я-2) пассажир - лицо, кроме водителя, находящееся в транспортном
        средстве, а также лицо, которое входит в транспортное средство или
        выходит из транспортного средства;<br />
        <br />
        я-3) претендент - лицо, которое официально подало документы для участия
        в конкурсе;<br />
        <br />
        я-4) перевозчик - лицо, осуществляющее транспортное и экспедиционное
        обслуживание пассажиров на основании соответствующих транспортных
        документов, установленных действующим законодательством Приднестровской
        Молдавской Республики;<br />
        <br />
        я-5) периодичность обслуживания регулярного маршрута (рейса) -
        установленный период времени обслуживания регулярного маршрута (рейса).
        Периодичность подразделяется на сезонность (сезонный или всесезонный) и
        регулярность обслуживания (ежедневно или по конкретным дням недели);<br />
        <br />
        я-6) производственно-техническая база - объект, состоящий из комплекса
        зданий и сооружений для обслуживания и содержания в надлежащем состоянии
        транспортных средств, включающий территорию для стоянки транспортных
        средств;<br />
        <br />
        я-7) путевой лист - документ первичного учета, выдаваемый перевозчиком
        водителю транспортного средства, предназначенный для ведения учета
        работы водителя и транспортного средства;<br />
        <br />
        я-8) расписание движения - поминутный график следования транспортного
        средства между остановочными пунктами (с указанием адресов в населенных
        пунктах) на пригородном, междугородном и международном маршруте или
        дифференцированные по времени суток и (или) дням недели интервалы
        следования транспортного средства между остановочными пунктами.<br />
        <br />
        я-9) рейс - путь следования транспортного средства по установленному
        маршруту от начального до конечного пункта и обратно.<br />
        <br />
        я-10) резервный автобус - автобус, прошедший технический осмотр,
        находящийся в технически исправном состоянии, который обязан выйти на
        маршрут для замены выбывшего автобуса;<br />
        <br />
        я-11) регулярные перевозки - систематические перевозки пассажиров и
        багажа, выполняемые по заранее утвержденному маршруту (рейсу), тарифам и
        установленной расписанием периодичностью и интервалам движения.<br />
        <br />
        я-12) ручная кладь - вещи пассажира, разрешенные к бесплатной перевозке
        в салоне транспортного средства;<br />
        <br />
        я-13) разрешение на автомобильную перевозку пассажиров по регулярному
        городскому маршруту - документ, выданный органом местного
        государственного управления, на каждую единицу подвижного состава
        перевозчика, подтверждающий право перевозчика на перевозку пассажиров и
        багажа по городскому регулярному маршруту.<br />
        <br />
        я-14) разрешение на автомобильную перевозку пассажиров по регулярному
        пригородному, междугородному, международному маршруту - документ,
        выданный исполнительным органом государственной власти, к ведению
        которого отнесены вопросы управления транспортом, подтверждающий право
        перевозчика на перевозку пассажиров и багажа по указанному маршруту
        (рейсу), с указанием перевозчика, категории регулярного маршрута,
        расписания и схемы движения.<br />
        <br />
        я-15) срыв регулярного рейса - невыполнение с начального пункта
        отправления в соответствии с установленным расписанием движения маршрута
        (рейса) по вине перевозчика;<br />
        <br />
        я-16) схема маршрута - графическое условное изображение регулярного
        маршрута с указанием остановочных пунктов, расстояний между ними, а
        также характерных ориентиров (развилок дорог, перекрестков,
        железнодорожных переездов, мостов, тоннелей и так далее);<br />
        <br />
        я-17) транспорт общего пользования - автомобильный транспорт, городской
        наземный электрический транспорт, используемый для выполнения перевозки
        пассажиров и багажа на основании публичного договора, то есть по
        обращению любого физического или юридического лица по регулярным
        маршрутам (рейсам);<br />
        <br />
        я-18) транспортное средство - это техническое устройство для перевозки
        пассажиров и багажа по автомобильным дорогам, приводимое в движение
        двигателем (электродвигателем);<br />
        <br />
        я-19) трафарет - указатель наименования регулярного маршрута для
        информирования пассажиров о маршруте (рейсе) и режиме следования
        автобуса;<br />
        <br />
        я-20) час пик - временные промежутки, когда в населенных пунктах
        происходит массовое передвижение пассажиров - с 07:00 до 09:00 часов и с
        16:30 до 18:30 часов;<br />
        <br />
        я-21) электрический транспорт - вид транспорта, в котором для движения
        используется один или несколько тяговых электрических двигателей,
        предназначенный для перевозки пассажиров и багажа по автомобильным
        дорогам.<br />
        <br />
        я-22) реестр регулярных маршрутов - перечень регулярных маршрутов и
        рейсов, расписаний движения, публикуемый в средствах массовой информации
        исполнительным органом государственной власти, к ведению которого
        отнесены вопросы управления транспортом, фиксирующий право перевозки по
        регулярному маршруту (рейсу) на 10 (десять) лет, с обязательным
        указанием: наименования перевозчика, времени отправления с начального и
        конечного пунктов регулярного маршрута и периодичности обслуживания,
        включающий также сведения о регулярных маршрутах (рейсах), выставляемых
        на конкурс;<br />
        <br />
        я-23) нерегулярные перевозки - перевозки, выполняемые на условиях,
        определенных в каждом конкретном случае по договорам или заказам.<br />
        <br />
        я-24) социально значимый регулярный маршрут (рейс) - это регулярный
        пригородный маршрут (рейс), организованный в целях обеспечения
        транспортного сообщения с населенными пунктами, объектами социальной
        сферы, а также с другими видами транспорта, разрыв с которыми может
        привести к нарушению функционирования систем жизнеобеспечения,
        ограничению свободы передвижения населения и вызвать иные негативные
        последствия.<br />
        <br />
        3. Классификация регулярных перевозок:<br />
        <br />
        а) по протяженности и территории прохождения:<br />
        <br />
        1) городские - перевозка пассажиров и багажа в пределах города или
        населенного пункта.<br />
        <br />
        2) пригородные - перевозка за пределы города (населенного пункта) на
        расстояние до 50 км включительно в пределах Приднестровской Молдавской
        Республики;<br />
        <br />
        3) междугородные - перевозка между городами Приднестровской Молдавской
