        254. Государственный контроль и надзор за деятельностью регулярного
        автомобильного транспорта общего пользования осуществляется в порядке,
        установленном действующим законодательством Приднестровской Молдавской
        Республики.<br />
        <br />
        255. Перед проведением проверки лицо, осуществляющее мероприятие по
        контролю (надзору), обязано по требованию предъявить водителю или
        кондуктору служебное удостоверение. Водитель и кондуктор обязаны
        предъявить все затребованные для контроля и предусмотренные действующим
        законодательством документы, оказать помощь в его проведении, выполнить
        указания проверяющего.<br />
        <br />
        256. Результаты мероприятия по контролю (надзору) оформляются в
        соответствии с порядком, установленным действующим законодательством
        Приднестровской Молдавской Республики.<br />
        <br />
        257. Ежемесячно органы, осуществляющие государственный контроль и надзор
        за деятельностью регулярного автомобильного транспорта общего
        пользования, в срок до 10 числа месяца, следующего за отчетным,
        представляют в уполномоченный исполнительный орган государственной
        власти, в ведении которого находятся вопросы управления транспортом
        (орган местного государственного управления), информацию о нарушениях
        действующего законодательства Приднестровской Молдавской Республики в
        сфере транспорта.
      </p>
