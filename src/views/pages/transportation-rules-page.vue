        б) изменения расписания не более чем на 20 минут и схемы движения по
        маршруту (рейсу), если не затрагивается деятельность другого
        перевозчика, который осуществляет перевозки в данном направлении (за
        исключением международных маршрутов);<br />
        <br />
        в) изменения места отправления с одного автовокзала (автостанции) на
        другой в населенных пунктах, где имеется несколько автовокзалов и
        автостанций;<br />
        <br />
        г) изменения периодичности обслуживания регулярного международного
        маршрута (рейса), и этим не затрагивается деятельность другого
        перевозчика, который осуществляет регулярные перевозки в данном
        направлении;<br />
        <br />
        д) закрытия или прекращения обслуживания регулярного маршрута.<br />
        <br />
        е) при открытии нового регулярного международного маршрута или новых
        рейсов на существующем регулярном международном маршруте, если
        перевозчик предоставил документы в полном объеме в соответствии с
        требованиями настоящих Правил;<br />
        <br />
        ж) в случае если перевозчик на праве временного обслуживания выполняет
        регулярный междугородный маршрут (рейс) более одного года и при
        отсутствии случаев, предусмотренных подпунктами 1, 2, 4, 5, 8 подпункта
        "б" пункта 68 настоящих Правил;<br />
        <br />
        141. Право временного закрытия (прекращения обслуживания) регулярного
        пригородного, междугородного, международного маршрутов (рейсов), в том
        числе транзитного, по причине неблагоприятных дорожных и
        метеорологических условий принадлежит исполнительному органу
        государственной власти, к ведению которого отнесены вопросы управления
        транспортом.<br />
        <br />
        142. Об открытии, закрытии или изменении регулярных маршрутов (рейсов)
        операторы автомобильных перевозок извещают пассажиров на всех
        автовокзалах, автостанциях, диспетчерско-кассовых пунктах или с помощью
        средств массовой информации.<br />
        <br />
        143. Договор об обслуживании регулярных маршрутов (рейсов) автомобильных
        перевозок пассажиров и багажа может быть расторгнут в одностороннем
        порядке по инициативе исполнительного органа государственной власти, к
        ведению которого отнесены вопросы управления транспортом (органа
        местного государственного управления) в случаях, предусмотренных
        действующим законодательством.<br />
        <br />
        144. Действие договора об обслуживании регулярных маршрутов (рейсов)
        автомобильных перевозок пассажиров и багажа может быть прекращено в
        одностороннем порядке по инициативе перевозчика. В этом случае
        перевозчик обязан уведомить исполнительный орган государственной власти,
        в ведении которого находятся вопросы управления транспортом (органы
        местного государственного управления), о намерении расторгнуть договор
        об обслуживании регулярных маршрутов (рейсов) автомобильных перевозок
        пассажиров и багажа не позднее чем за 30 (тридцать) дней до его
        расторжения.<br />
        <br />
        145. Договор об обслуживании регулярных маршрутов (рейсов) автомобильных
        перевозок пассажиров и багажа также может быть расторгнут по соглашению
        сторон.<br />
        <br />
        146. В день прекращения действия договора об обслуживании регулярных
        маршрутов (рейсов) автомобильных перевозок пассажиров и багажа на
        обслуживание конкретных маршрутов (рейсов) исполнительный орган
        государственной власти, в ведении которого находятся вопросы управления
        транспортом (орган местного государственного управления), аннулирует
        действие разрешения (разрешений) на автомобильную перевозку пассажиров
        по регулярному маршруту с уведомлением соответствующего оператора
        автомобильных перевозок и объявляет новый конкурс на обслуживание
        регулярных маршрутов (рейсов).
