      <h3 class="medium is-adaptive">
        23. Международные нерегулярные перевозки
      </h3>
      <p class="regular is-adaptive align-j">
        276. Нерегулярные международные перевозки включают в себя:<br />
        <br />
        a) туры "с закрытыми дверями", то есть перевозки, когда одно и то же
        транспортное средство используется для перевозки одной и той же группы
        пассажиров в течение всего рейса и доставляет ее обратно на место
        отправления;<br />
        <br />
        б) перевозки пассажиров из начального пункта, расположенного на
        территории Приднестровской Молдавской Республики, до пункта назначения,
        расположенного на территории другого государства, где все пассажиры
        будут высажены, а транспортное средство будет возвращено в начальный
        пункт без пассажиров, пустым;<br />
        <br />
        в) перевозки, когда транспортное средство из начального пункта,
        расположенного на территории Приднестровской Молдавской Республики,
        будет следовать без пассажиров для их перевозки в Приднестровскую
        Молдавскую Республику.<br />
        <br />
        277. Хозяйствующие субъекты независимо от формы собственности
        осуществляют нерегулярные международные перевозки пассажиров только при
        наличии лицензии и контрольного документа (пассажирского карнета).<br />
        <br />
        278. Контрольный документ (пассажирский карнет) является документом
        строгой отчетности, оформляется на каждую поездку. После выполнения
        перевозок контрольный документ (пассажирский карнет) подлежит возврату
        исполнительному органу государственной власти, в ведении которого
        находятся вопросы управления транспортом.<br />
        <br />
        279. Выдача контрольного документа (пассажирского карнета)
        осуществляется исполнительным органом государственной власти, в ведении
        которого находятся вопросы управления транспортом.
      </p>
