      <h3 class="medium is-adaptive">
        19. Порядок оформления претензий и исков
      </h3>
      <p class="regular is-adaptive align-j">
        249. В случае утраты или порчи багажа, повреждения багажа и (или) ручной
        клади пассажира по вине перевозчика составляется акт об утере, недостачи
        или повреждения багажа, ручной клади, который является основанием для
        возмещения причиненного ущерба в установленном порядке.<br />
        <br />
        250. Акт об утере, недостачи или повреждении багажа составляется
        представителем перевозчика, оператора пассажирских перевозок и
        пассажиром, правоохранительных органов.<br />
        <br />
        251. Пассажир имеет право предъявить претензию при утрате, недостачи или
        повреждении багажа, а также при причинении вреда жизни, здоровью
        перевозчику в течение 7 (семи) дней со дня прибытия пассажира или багажа
        в пункт назначения. В случае пропуска пассажиром указанного срока
        предъявления претензии по уважительным причинам данный срок может быть
        восстановлен.<br />
        <br />
        252. Перевозчик обязан в течение 30 (тридцати) дней со дня предъявления
        претензии со всеми необходимыми документами рассмотреть ее и сообщить
        заявителю о результатах. В случае полного или частичного отклонения
        претензии перевозчик обязан в своем уведомлении указать причины и
        возвратить документы, приложенные к претензии.<br />
        <br />
        253. По всем спорным вопросам, возникающим в связи с перевозкой
        пассажиров и багажа в международном сообщении, пассажир имеет право
        обратиться в суды государства, на территории которого произошло событие,
        повлекшее вред либо ущерб, или расположенных по юридическому адресу
        перевозчика или пассажира, а также в пунктах отправления или прибытия
        пассажира.
      </p>
    </div>
