        <br />
        в) иметь в наличии документы, подтверждающие право осуществлять
        автомобильные перевозки пассажиров и багажа на регулярных маршрутах.<br />
        <br />
        г) не отклоняться от установленной схемы маршрута и соблюдать расписание
        (интервал) движения и режим работы маршрута.<br />
        <br />
        д) проверять наличие и соответствие билетов на проезд и перевозку
        багажа;<br />
        <br />
        е) содержать транспортные средства в надлежащем техническом и санитарном
        состоянии;<br />
        <br />
        ж) иметь опрятный вид и поддерживать чистоту и порядок на своем рабочем
        месте и в салоне транспортного средства;<br />
        <br />
        з) быть вежливым и внимательным к пассажирам, непримиримым к недостаткам
        в обслуживании пассажиров и своевременно их устранять;<br />
        <br />
        и) останавливать транспортное средство по требованию лиц, осуществляющих
        контроль, выполнять их указания и оказывать им помощь при осуществлении
        контроля;<br />
        <br />
        к) перед выездом на маршрут пройти предрейсовое медицинское
        освидетельствование и технический осмотр транспортного средства;<br />
        <br />
        л) продавать билеты в пути следования пассажирам и вносить сведения о
        проданных билетах в билетно-учетный лист только до начала движения
        автобуса (кроме городских маршрутных такси);<br />
        <br />
        м) следить за выполнением пассажирами настоящих Правил, размещением
        багажа и ручной клади, принимать, размещать и выдавать багаж на
        остановках, предусмотренных расписанием;<br />
        <br />
        н) соблюдать правила пограничного, таможенного и других видов контроля
        государств, по территории которых совершается международная
        перевозка;<br />
        <br />
        о) своевременно оповещать и подготавливать пассажиров для прохождения
        контроля при пересечении границы;<br />
        <br />
        п) в случае дорожно-транспортного происшествия по возможности оказать
        помощь пострадавшим и оповестить соответствующие органы о
        случившемся;<br />
        <br />
        р) принять меры по оказанию первой медицинской помощи пассажиру,
        нуждающемуся в ней, а также доставить его в ближайший пункт неотложной
        помощи;<br />
        <br />
        с) обеспечивать соблюдение пассажирами настоящих Правил;<br />
        <br />
        т) соблюдать установленный режим труда и отдыха;<br />
        <br />
        у) проверять перед выездом на маршрут и систематически во время рейса
        техническую исправность автобуса, контролировать перед началом движения
        закрытие дверей и багажных отсеков;<br />
        <br />
        ф) включать отопление салона при температуре воздуха ниже +5°С или по
        просьбам пассажиров;<br />
        <br />
        х) знать расположение остановочных пунктов и опасных участков на
        маршруте, расположение пунктов оказания технической и медицинской
        помощи;<br />
        <br />
        ц) не допускать переполнения салона и багажного отсека сверх
        установленной нормы для конкретного типа транспортного средства;<br />
        <br />
        ч) объявлять остановки (при междугородных и международных перевозках
        время стоянки на них), а при изменении маршрута (рейса) объявлять об
        этом на каждом остановочном пункте;<br />
        <br />
        ш) выполнять автомобильную перевозку пассажиров в регулярном сообщении в
        соответствии с утвержденным расписанием движения, а при выполнении
        перевозок городскими маршрутными такси - согласно интервалам движения с
        соблюдением утвержденного времени работы маршрута и схемы движения.<br />
        <br />
        щ) осуществлять посадку (высадку) после остановки транспортного средства
        лишь с посадочной площадки, а в случае отсутствия такой площадки - с
        тротуара или обочины. Если это невозможно, то со стороны проезжей части
        при условии, что это будет безопасно и не создаст препятствий другим
        участникам движения;<br />
        <br />
        ы) осуществлять перевозку пассажиров с соблюдением требований
        безопасности.<br />
        <br />
        43. Водитель имеет право:<br />
        <br />
        а) требовать от пассажиров выполнения ими своих обязанностей;<br />
        <br />
        б) не допускать к поездке или высадить на ближайшей остановке пассажира,
        нарушавшего требования настоящих Правил и обязанностей по договору об
        обслуживании регулярных маршрутов (рейсов);<br />
        <br />
        в) не выдавать багаж без дополнительного разбирательства, если не
        предъявлено документальное подтверждение сдачи багажа;<br />
        <br />
        г) получать от органов, осуществляющих контроль, разъяснения о
        допущенных нарушениях;<br />
        <br />
        д) присутствовать во время проверки транспортного средства и багажа;<br />
        <br />
        е) обжаловать в установленном законодательством порядке неправомерные
        действия контролирующих лиц;<br />
        <br />
        ж) получать плату за автомобильную перевозку пассажира, за провоз
        оплачиваемого багажа и оплачиваемой ручной клади в местах, где нет
        билетных касс;<br />
        <br />
        44. Водителю запрещается:<br />
        <br />
        а) начинать движение до полного закрытия дверей и открывать их до полной
        остановки транспортного средства;<br />
        <br />
        б) нарушать без уважительных причин интервал, время работы, расписание и
        схему движения маршрута (рейса);<br />
        <br />
        в) перевозить на передних сидениях транспортных средств детей в возрасте
        до 12 лет;<br />
        <br />
        г) перевозить в салоне не предусмотренные техническими условиями
        транспортного средства вещи, запчасти, емкости с техническими
        жидкостями;<br />
        <br />
        д) курить в салоне автобуса;<br />
        <br />
        е) отправляться в рейс без путевой документации и документов, дающих
        право на перевозку пассажиров;<br />
        <br />
        ж) перевозить в салоне автобуса на пригородных, междугородных и
        международных маршрутах (рейсах) больше пассажиров, чем предусмотрено
        технической характеристикой транспортного средства;<br />
        <br />
        з) осуществлять безбилетный провоз пассажиров и провоз пассажиров с
        билетами, не внесенными в билетно-учетный лист (кроме городских
        маршрутов);<br />
        <br />
        и) реализовывать билеты во время движения, а также в местах, где имеются
        билетные кассы;<br />
        <br />
        к) пользоваться аудио и телеаппаратурой без согласия пассажиров.<br />
        <br />
        л) осуществлять перевозку пассажиров в городском сообщении при
        обслуживании пригородных, междугородных и международных маршрутов
        (рейсов) в случае, если в данном населенном пункте осуществляются
        регулярные городские перевозки транспортом общего пользования.<br />
        <br />
        м) осуществлять остановку транспортного средства в целях, не связанных с
        автомобильной перевозкой пассажиров и багажа.<br />
        <br />
        45. Ответственность водителя за вред, причиненный жизни или здоровью
        пассажира, определяется в соответствии действующим законодательством
        Приднестровской Молдавской Республики.<br />
        <br />
        Водитель может быть освобожден от ответственности в случае, если
        докажет, что вред возник вследствие непреодолимой силы или нарушения
        пассажиром настоящих Правил.
      </p>
